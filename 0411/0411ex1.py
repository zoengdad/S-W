#if-else
jumsu=input("점수 입력:") #점수 정수로 입력
if jumsu>=80: #선택 조건
    print("합격") #조건이 참 일때만 실행
else:
    print("불합격") #조건이 거지 일때만 실행
print("수고하셨습니다.") #조건이 관계잆이 항상 실행